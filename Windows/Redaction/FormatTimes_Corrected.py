def formatTime(s):    
    h = int(s / 3600)
    m = int((s - (h*3600))/ 60)
    s = round(s % 60)
    if h > 0:    
        return str(h)+"h"+str(m)+"m"
    if m > 0:
        return str(m)+"m"+str(s)+"s"
    return str(s)+"s"
    
def s(A,B):
    return [ A[i]+B[i] for i in range(len(A))]

def p(A):
    print(formatTime(A[0]))

